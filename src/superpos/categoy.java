    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
            DefaultTableModel d1 = (DefaultTableModel)jTable1.getModel();
            int selectIndex = jTable1.getSelectedRow();
            int id = Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());
        
            String category = txtcat.getText();
            String status = txtStatus.getSelectedItem().toString();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3308/superstore","root","");
            
            pst = con.prepareStatement("update category set category=?,status=? where id= ?");
            
            pst.setString(1,category);
            pst.setString(2,status);
            pst.setInt(3, id);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Category Updated");
            table_update();
            txtcat.setText("");
            txtStatus.setSelectedIndex(-1);
            txtcat.requestFocus();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(categoy.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(categoy.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        
        
        
        
    }//GEN-LAST:event_updateButtonActionPerformed
       private void table_update(){
        
            int c;
            
                try {
                    Class.forName("com.mysql.jdbc.Driver");
                    con = DriverManager.getConnection("jdbc:mysql://localhost:3308/superstore","root","");
                    
                    
                    pst = con.prepareStatement("select * from category");   
                    ResultSet rs = pst.executeQuery();
                    ResultSetMetaData rsd = rs.getMetaData();
                    c = rsd.getColumnCount();
                    
                    DefaultTableModel d = (DefaultTableModel)jTable1.getModel();
                    d.setRowCount(0);
                    
                    
                    while(rs.next())
                    {
                        Vector v2 = new Vector();
                        
                        for (int i =1 ; i<=c ; i++)
                        {
                            v2.add(rs.getString("id"));
                            v2.add(rs.getString("category"));
                            v2.add(rs.getString("status"));
                        }
                        
                        d.addRow(v2);
                        
                        
                    }
                } 
                catch (ClassNotFoundException ex) {
                    Logger.getLogger(categoy.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                catch (SQLException ex) {
                    Logger.getLogger(categoy.class.getName()).log(Level.SEVERE, null, ex);
                }
            
        }
        
