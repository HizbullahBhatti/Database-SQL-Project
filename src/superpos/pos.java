            pst = con.prepareStatement(query,Statement.RETURN_GENERATED_KEYS);
            
            
            pst.setString(1, date);   
            pst.setString(2, cashier); 
            pst.setString (3, subtot);
            pst.setString (4, pay);  
            pst.setString (5, bal); 
            pst.executeUpdate();
            
            ResultSet generatedKeyResult = pst.getGeneratedKeys();
            
            if (generatedKeyResult.next()) 
                {
                    lastinsertid = generatedKeyResult.getInt(1);
                }    
                    
                //JOptionPane.showMessageDialog(this, lastinsertid);
                    int rows=jTable1.getRowCount();
                    String query1 = "Insert into sales_product(sales_id,product_id,sell_price,qty,total)values(?,?,?,?,?)";    
                    pst = con.prepareStatement(query1);
                    
                    String product_id = "";
                    String price ="";
                    String qty="";
                    int total=0;
                    
