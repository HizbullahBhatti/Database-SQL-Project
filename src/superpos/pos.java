        private void pos()
        {
            String name =txtprocode.getText();     
            
        try {       
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3308/superstore","root","");
                pst = con.prepareStatement("SELECT *FROM product where barcode=?");
                pst.setString(1,name);
                rs = pst.executeQuery();
                
                while(rs.next())
                {
                    int currentqty;
                    currentqty = rs.getInt("qty");
                   
                    int price =  Integer.parseInt(txtprice.getText());
                    int qtynew =  Integer.parseInt(txtqty.getText());      
                    int tot = price * qtynew;
                    
                    if(qtynew >= currentqty)
                    {
                       JOptionPane.showMessageDialog(null,"Available Product" + " = " + currentqty);
                       JOptionPane.showMessageDialog(null, "Qty is not Enough");
                    }    
                    
                    else
                   {  
                      model = (DefaultTableModel)jTable1.getModel();      
                      model.addRow(new Object[]
                   {         
                      txtprocode.getText(),
                      txtproname.getText(),
                      txtprice.getText(),
                      txtqty.getText(),
                      tot,
                  }); 
                      
                    int sum = 0;
                    for(int i = 0; i < jTable1.getRowCount(); i++)
                    {
                        sum = sum + Integer.parseInt(jTable1.getValueAt(i, 4).toString());
                    }   
                    txtsub.setText(Integer.toString(sum)); 
                    //txtsub.setText(Integer.toString(sum));
                    
                      txtprocode.setText("");
                      txtproname.setText("");
                      txtprice.setText("");
                      txtqty.setText("");
                      txtprocode.requestFocus();
                    
                }}
        
        
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
        }
                
        
        }

